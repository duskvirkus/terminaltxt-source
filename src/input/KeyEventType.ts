/**
 * Enum of Key Event types outlined by javascript. More information can be found in the mozilla documentation: [keydown](https://developer.mozilla.org/en-US/docs/Web/API/Document/keydown_event), [keyup](https://developer.mozilla.org/en-US/docs/Web/API/Document/keyup_event), [keypress](https://developer.mozilla.org/en-US/docs/Web/Events/keypress).
 */
export enum KeyEventType {

  /**
   * Corresponds to document.keydown event.
   */
  KEYDOWN,

  /**
   * Corresponds to document.keyup event.
   */
  KEYUP,

  /**
   * Corresponds to document.keypress event.
   */
  KEYPRESS,

}